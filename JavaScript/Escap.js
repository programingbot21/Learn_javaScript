let str = "Hello,\nThis is a new line.\n\tThis is indented with a tab.";
console.log(str);

// Using quotes in a string
let quoteStr = "She said, \"Hello, World!\"";
console.log(quoteStr);

// Including a backslash in a string
let path = "C:\\Users\\John\\Documents";
console.log(path);

// Using a single quote within a single-quoted string
let singleQuoteStr = 'It\'s a beautiful day!';
console.log(singleQuoteStr);

// Unicode escape sequence
let unicodeStr = "\u0048\u0065\u006C\u006C\u006F"; // Represents "Hello"
console.log(unicodeStr);